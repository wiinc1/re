<template>
  <div>
    <h1>Enroll</h1>

    <input
      type="email"
      name="email"
      v-model="email"
      placeholder="email" />
    <br>
    <input
      type="password"
      name="password"
      v-model="password"
      placeholder="password" />
    <br>
    <button
      @click="enroll">
      Enroll
    </button>
  </div>
</template>

<script>
import AuthenticationService from '@/services/AuthenticationService'
export default {
  data () {
    return {
      email: '',
      password: ''
    }
  },
  methods: {
    async enroll () {
      await AuthenticationService.enroll({
        email: this.email,
        password: this.password
      })
    }
  }
}
</script>

